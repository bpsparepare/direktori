import '../../domain/entities/direktori.dart';
import '../../../map/data/models/direktori_model.dart' as MapDirektori;

class DirektoriModel extends Direktori {
  const DirektoriModel({
    required super.id,
    required super.idSbr,
    required super.namaUsaha,
    super.alamat,
    required super.idSls,
    required super.kegiatanUsaha,
    super.skalaUsaha,
